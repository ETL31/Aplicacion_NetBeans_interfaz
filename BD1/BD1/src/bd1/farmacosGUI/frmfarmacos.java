        Conexion objConexion= new Conexion();
        
        farmacosBL oFarmacos = recuperarDatosGUI();
        
        String strSentenciaInsert;
        strSentenciaInsert = String.format("INSERT INTO Farmacos (Codigo, Descripcion,Abreviatura,Stock, Precio) "
                + "VALUES (null, 'Loratadina 50mg','LTDNA50MG',null,null)",oFarmacos.getCodigo(), oFarmacos.getDescripcion(), oFarmacos.getAbreviatura());
     
        
        objConexion.ejecutarSentenciaSQL(strSentenciaInsert);
        
        try{
            ResultSet resultado=objConexion.consultarRegistros("SELECT * FROM Farmacos");
            
            while(resultado.next()){
                
                System.out.println(resultado.getString("Codigo"));
                System.out.println(resultado.getString("Descripcion"));
                System.out.println(resultado.getString("Abreviatura"));
                System.out.println(resultado.getString("Stock"));
                System.out.println(resultado.getString("Precio"));
            }
            
        }catch(Exception e){
            
            System.out.println(e);
        }
        
