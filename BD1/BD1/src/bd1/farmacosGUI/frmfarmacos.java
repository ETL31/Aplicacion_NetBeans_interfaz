public farmacosBL recuperarDatosGUI(){
    
    farmacosBL oFarmacos= new farmacosBL();
    
    int Codigo = (txtCodigo.getText().isEmpty()?0: Integer.parseInt(txtCodigo.getName()));
    int Stock =  (txtStock.getText().isEmpty()?0: Integer.parseInt(txtStock.getName()));
    float Precio = (txtPrecio.getText().isEmpty()?0: Integer.parseInt(txtPrecio.getName()));
    
    oFarmacos.setCodigo(Codigo);
    oFarmacos.setAbreviatura(txtAbreviatura.getText());
    oFarmacos.setDescripcion(txtDescripcion.getText());
    oFarmacos.setStock(Stock);
    oFarmacos.setPrecio(Precio);
    
    return oFarmacos;
    
}
        
